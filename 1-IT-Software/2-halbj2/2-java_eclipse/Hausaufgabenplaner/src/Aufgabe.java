
public class Aufgabe {

  private String titel;
  private String fach;
  private int tag;
  private int monat;
  private String beschreibung;
  private boolean erledigt;
  
  
  public String getTitel() {
	  return titel;
  }
  
  public void setTitel(String pTitel) {
	  titel = pTitel;
  }
  
  
  public String getFach() {
	  return fach;
  }
  
  public void setFach(String pFach) {
	  fach = pFach;
  }
  
  
  public int getTag() {
	  return tag;
  }
  
  public void setTag(int pTag) {
	  tag = pTag;
  }
  
  
  public int getMonat() {
	  return monat;
  }
  
  public void setMonat(int pMonat) {
	  monat = pMonat;
  }
  
  
  public String getBeschreibung() {
	  return beschreibung;
  }
  
  public void setBeschreibung(String pBeschreibung) {
	  beschreibung = pBeschreibung;
  }
  
  
  public boolean getErledigt() {
	  return erledigt;
  }
  
  public void setErledigt(Boolean pErledigt) {
	  erledigt = pErledigt;
  }
  
}
